# list -> array, mengakses dengan menggunakan index

data_list = ["ucup","otong","udin"]
print(data_list[0])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    'cup':'ucup',
    'tong':'otong',
    'din':'udin',
    'no':12,
    'list':data_list
}

print(data_dict['cup'])
print(data_dict['list'])